        IDoseguro.jTextDNI.setText(IntAccesoDoc.jTextDNI.getText());
        if(tablaSeguro.contains(0, 3)== true){
        IDoseguro.jTextMATRICULA.setText((String)tablaSeguro.getValueAt(0, 3));
        }        
